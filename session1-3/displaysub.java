
public class displaysub {
    public static void main(String[] args) {
        int num1 = 49;
        int num2 = 22;
        int sub = num1 - num2;
        System.out.print("The difference of");
        System.out.print(num1);
        System.out.print(" and ");
        System.out.print(num2);
        System.out.print(" is ");
        System.out.print(sub);
        System.out.println(".");
        int num3 = 2;
        sub = num1 - num2 - num3;
        System.out.println("The difference of " + num1 + ", " + num2 + " and "
                + num3 + " is " + sub + ".");
    }
}


